#Write a program which accept two number and print addition, subtraction, multiplication and division.
def calculate(a, b):
    print("Addition:", a + b)
    print("Subtraction:", a - b)
    print("Multiplication:", a * b)
    print("Division:", a / b)

calculate(10, 5)

# Output:
# #Addition: 15
# Subtraction: 5
# Multiplication: 50
# Division: 2.0