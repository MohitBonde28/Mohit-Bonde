#Write a program which contain one function chkGreater() that accepts two numbers 
# and prints the greater number. 
# Input: 10 20 
# Output: 20 is greater.

def chkGreater(no1, no2):
    if no1 > no2:
        print(no1, "is greater")
    else:
        print(no2, "is greater")

chkGreater(10, 20)

#Output:
#20 is greater
